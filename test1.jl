function cal_M2(M::Array{Float64,1}, Q::Int64)
    M2 = 0
    for i = 1:Q
        for j = 1:Q
            M2 += M[i] * M[j] * (Q * ==(M[i], M[j]) - 1) / (Q - 1)
        end
    end
    return M2
end

println(cal_M2([1.0, 2.0], 2))

function cal_M(conf::Array{Int64,2}, Q::Int64, L::Int64)
    M = zeros(Q)
    for s in conf
        M[s] += 1
    end
    M = M / L^2
    return M
end

